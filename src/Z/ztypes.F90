MODULE ztypes
  USE atypes
  IMPLICIT NONE

  ABSTRACT INTERFACE
     SUBROUTINE ASRT(NN, NM, DZ, INFO)
       USE atypes
       IMPLICIT NONE
       INTEGER, INTENT(IN) :: NN, NM
       TYPE(AW), INTENT(INOUT) :: DZ(NM)
       INTEGER(KIND=DWP), INTENT(OUT) :: INFO
     END SUBROUTINE ASRT
  END INTERFACE

  ABSTRACT INTERFACE
     SUBROUTINE ANCP(N, NN, NM, DZ, N_2, SL, STEP, INFO)
       USE atypes
       IMPLICIT NONE
       INTEGER, INTENT(IN) :: N, NN, NM, N_2
       TYPE(AW), INTENT(INOUT) :: DZ(NM)
       INTEGER, INTENT(OUT) :: SL, STEP(N_2)
       INTEGER(KIND=DWP), INTENT(OUT) :: INFO
     END SUBROUTINE ANCP
  END INTERFACE

  TYPE ZPROC
#ifdef USE_SUN
     INTEGER :: NCP
     INTEGER :: TRU
     INTEGER :: SRT
#else
     PROCEDURE(ANCP), POINTER, NOPASS :: NCP
     PROCEDURE(ATRU), POINTER, NOPASS :: TRU
     PROCEDURE(ASRT), POINTER, NOPASS :: SRT
#endif
  END TYPE ZPROC

CONTAINS
END MODULE ztypes
